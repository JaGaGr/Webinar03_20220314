package ex3_operators_examples;

public class RelativeOperatorsExamples {
    public static void main(String[] args) {
        int i = 1, j = 2, k = 1, l = 0;

        System.out.println(i > j);

        System.out.println(i < j);

        System.out.println(i >= j);

        System.out.println(i >= 1);

        System.out.println(i <= l);

        System.out.println(i == j);

        System.out.println(i == l);

        System.out.println(i != j);

        System.out.println(i != k);
    }
}
